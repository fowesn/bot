	        1. с указанием темы\r\n",
	        "В этом случае нужно написать \"задание\" и добавить к команде название темы, по которой ты хочешь получить задание. Например, написав команду \"задание системы счисления\", ты получишь задание по теме \"системы счисления\". Если у тебя нет уверенности в названии темы или ты просто не знаешь, на какую тему задание можно попросить, ты можешь спросить это у меня командой \"темы\". Чтобы получить справку по это команде, напиши \"справка темы\".\r\n
	        2. с указанием номера из КИМа\r\n",
	        "Если ты хочешь получить типовое задание по номеру в КИМе, нужно написать мне \"задание\" и номер из КИМа. Например, чтобы получить типовое задание 5, напиши мне \"задание 5\", и я пришлю тебе такое задание.\r\n
	        3. без указания темы или номера из КИМа\r\n
	        Чтобы получить задание без указания темы или номера, напиши мне просто \"задание\" и ничего не добавляй. Я пришлю тебе задание, которое выберу сам. Другими словами, ты получишь случайное задание из всех заданий, что у меня есть.\r\n",
            "Обычно я выбираю для тебя задание из всех, которые у меня есть, но ты можешь попросить у меня присылать тебе те задания, которые появились позже определённого года. Для того чтобы узнать, как это сделать, напиши мне \"справка год\""
        );

    }
    private static function answer()
    {
        return array(
            "Ты можешь использовать эту команду, чтобы получить правильный ответ на любое задание, которое я тебе ранее выдал. Для этого напиши мне \"ответ\" и укажи номер задания. Например, если я выдавал тебе задание с номером 12, то напиши мне \"ответ 12\", тогда я пришлю тебе ответ. Но учти, что если ты запросишь правильный ответ на задание, я не засчитаю в твоей статистике твой ответ на это задание, если ты пришлёшь его после того, как узнаешь правильный ответ у меня.",
            "Если ты хочешь отправить мне свой ответ на проверку, напиши мне номер задания, на который ты даёшь ответ, и сам ответ. Например, если ты хочешь ответить \"eadbfc\" на задание номер 12, напиши мне \"12 eadbfc\"",
            "Обрати внимание, что использовать нужно не номер задания из КИМа, а уникальный номер, который я назначил заданию, когда присылал его тебе."
        );
    }
    private static function incompleted()
    {
        return array(
            "Эта команда используется для получения списка всех нерешённых заданий (тех заданий, на которые я не получал правильный ответ и сам не присылал его тебе). Чтобы её использовать, напиши мне только одно слово \"задания\". Я пришлю тебе номера нерешённых тобой заданий, которые ранее выдавал тебе. Ты можешь воспользоваться этим списком, чтобы вспомнить, какие задания у тебя ещё не решены. ",
            "Чтобы получить текст задания, которое уже было выдано, ты можешь использовать команду \"условие\". Подробнее про неё я тебе напишу, если ты меня попросишь командой \"справка условие\"."
        );
    }
    private static function repeat()
    {
        return array(
            "Команда \"условие\" нужна, чтобы получить условие ранее выданного задания. Ты можешь использовать эту команду, написав мне \"условие\" и добавив номер задания, которое я уже выдавал тебе. Например, если ты хочешь получить условие задания 12, напиши мне \"условие 12\". Тогда я пришлю тебе текст этого задания, и ты сможешь вернуться к его решению. Если ты не помнишь номера заданий, я помогу тебе вспомнить, если ты попросишь меня об этом командой \"задания\". Подробнее про команду \"задания\" можешь спросить у меня командой \"справка задания\".",
            "Обрати внимание, что использовать нужно не номер задания из КИМа, а уникальный номер, который я назначил заданию, когда присылал его тебе."
        );
    }
    private static function analysis()
    {
        return array(
            "Ты можешь использовать эту команду, чтобы получить разбор любого задания, которое я тебе ранее выдал. Для этого напиши мне \"разбор\" и укажи номер задания. Например, если я выдавал тебе задание с номером 12, то напиши мне \"разбор 12\", тогда я пришлю тебе разбор этого задания. Помни, что разбор задания - это не ответ, а просто подсказка.",
            "После выдачи разбора я буду ждать от тебя ответа на задание и засчитаю твой ответ для твоей статистики, если он будет правильным :) Хотя если ты всё равно не знаешь, как правильно решить это задание, я могу сказать тебе правильный ответ. Для этого используй команду \"ответ\". Подробнее про эту команду ты можешь узнать, если напишешь мне \"справка ответ\".\r\n",
            "Обрати внимание, что использовать нужно не номер задания из КИМа, а уникальный номер, который я назначил заданию, когда присылал его тебе."
        );
    }
    private static function themes()
    {
        return array(
            "Эта команда предназначена для получения списка всех тем, по которым у меня есть задания. Напиши мне \"темы\", и я расскажу тебе о том, какие темы у меня есть. Чтобы получить задание по теме из списка, ты можешь использовать команду \"задание\". Чтобы получить подробности, напиши мне \"справка задание\"."
        );
    }
    private static function resources()
    {
        return array(
            "Я могу выдавать тебе задания и разборы заданий в разном виде, поэтому у меня есть команда, которая позволит тебе узнать список доступных ресурсов представления заданий и разборов. Напиши мне \"ресурсы\", и я расскажу тебе, какие ресурсы представления информации у меня есть. Чтобы выбрать один ресурс из списка, ты можешь использовать команду \"ресурс\". Для получения подробностей напиши мне \"справка ресурс\"."
