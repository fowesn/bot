        return array(
            "Команду \"ресурс\" ты можешь использовать, чтобы выбрать вид, в котором тебе будут приходить от меня задания и разборы. Напиши мне \"ресурс\" и добавь название ресурса, который ты хочешь. Например, если ты хочешь получать задания и разборы в виде изображений, напиши мне \"ресурс изображение\". ",
