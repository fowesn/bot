namespace MainModule\handler\message;
class Help
{
    public static function parse ($command)
    {
        switch ($command)
        {
            case 'задание':
                return self::task();
                break;
            case 'ответ':
                return self::answer();
                break;
            case 'задания':
                return self::incompleted();
                break;
            case 'условие':
                return self::repeat();
                break;
            case 'разбор':
                return self::analysis();
                break;
            case 'темы':
                return self::themes();
                break;
            case 'ресурсы':
                return self::resources();
                break;
            case 'ресурс':
                return self::resource();
                break;
            case 'год':
                return self::year();
                break;
            case 'статистика':
                return self::statistics();
                break;
            case 'справка':
                return self::help();
                break;
            default:
                return self::incorrect();
                break;

        }

    }
    private static function help()
    {
        return array(
            "Я могу подробно рассказать тебе о командах, которые понимаю. Для этого напиши мне \"справка [название команды]\". Вот список команд, о которых я тебе могу рассказать:\r\n
            1. задание\r\n
            2. задания\r\n
            3. ответ\r\n
            4. разбор\r\n
            5. условие\r\n
            6. темы\r\n
            7. ресурсы\r\n
            8. ресурс\r\n
            9. год\r\n
            10. статистика\r\n
            Например, если ты хочешь получить справку по команде \"статистика\", напиши мне \"справка статистика\""
        );
    }
    private static function task()
    {
        return array(
